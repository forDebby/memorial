          "Last week, I lost a dear friend, Deborah L Simmons, to cancer.
        </p>
        <p>
          In October 2018, Michael and I spent a night at the Simmons’ ranch near Pincher Creek, Alberta. We awoke to
          beautiful blue skies and softening early season snow. Deb suggested that we snowshoe up the ridge so we could
          really see the Gladstone Valley, the place her parents had made their home after leaving the North and a place
          that was equally special for her. I questioned the suggestion—Deb was in the middle of chemotherapy—but she
          assured me she felt up for it. I should not have worried. She bounded up the ridge, leaving me scrambling to
          catch up.
        </p>
        <p>
          I have told the story of that hike many times because it captured something of who Deb was: a person with
          limitless energy and enthusiasm; a person with the sunniest of dispositions, unwavering optimism, and a lust
          for life; a person with a deep love for the land and its caretakers; and a person who was happiest when others
          were along for the ride, even if it meant she had to help them get where she was going.
          Like so many in Debby’s network, we were colleagues first. We met in December 2016 when I was working at the
          NWT Recreation and Parks Association, supporting on the land programs. She approached me about doing some
          safety planning work with the leadership team for the first Dene Ts’ı̨lı̨ School. I explained my reticence
          about using conventional risk management tools for land-based programs. Deb liked the idea of seeing if we
          could do things differently. Over the next few years, we worked alongside Dene and Métis elders, knowledge
          keepers, and youth to develop a plan for the Sahtú Renewable Resources Board that was grounded in and
          regenerated Dene and Métis knowledges and practices related to safety. I learned so much through the process
          from Deb about how to be a good ally, as I watched her create space for discussion, ask good questions, listen
          intently, carefully document terms and concepts in Dene Kǝdǝ́, and laugh at herself.
          I was fortunate to be able to continue to work with and learn from Deb on a number of projects that were dear
          to her, including the Sahtú Nę K’ǝ́dıkǝ́ - Keepers of the Land guardian program; the public listening
          sessions on conserving caribou; the Sahtú COVID-19 response evaluation; and most recently, the petroleum
          histories project. The thread through these topically diverse projects was upholding Indigenous
          self-determination and governance and revitalizing land connections.
        </p>
        <p> I loved how Deb’s brain worked, even if I struggled to keep up. I loved her curiosity, which extended into
          so
          many disparate realms. I loved (and shared) her zeal for fifty per cent off food and free books. I loved that
          when she developed a passion for something, whether it was kefir, a bidet, vermicomposting, or a Biolite
          stove, she became a proselytizer on par with the most fanatical of religious acolytes (Deb managed to convince
          me of the value of all of these things). Maybe more than anything, I loved her laugh, and I worked hard to
          hear it whenever we were together.
        </p>
        <p> Michael and I visited the Gladstone Valley again this fall, this time with our son, Arn. From the time that
          Deb learned I was pregnant, she was a doting auntie, checking in on me by text and asking to see my growing
          belly during our Zoom calls. When he arrived in June, she fawned over photos and videos until she was finally
          able to cuddle him in person in August. I’m so thankful that Deb and Arn met each other; I’d like to think
          that in their three visits, he soaked up enough Auntie Debby love to last a lifetime.
          Losing Deb has been so hard. I’ve taken comfort in spending time with others who knew her and reading posts
          from people in her vast network. I’ve been thinking of all of the things that Deb still wanted to do and I
          hope that I can help realize some of those visions, but maybe even more importantly, I hope that in my life
          and work, I can walk as she did, lightly, with passion and a spring in her step, and with deep respect and
          love for land and people."
        </p>
